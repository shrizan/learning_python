course = "Python programming"
# this prints the length of string
print(len(course))

# print first character from the string
print(course[0])

# print last character from the string
print(course[-1])

# print index [0] to index [2]
print(course[0:3])

# print from index 1 to last character of the string
print(course[1:])

# print from zero index to upto index 2
print(course[:3])
